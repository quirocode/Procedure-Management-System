    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
    String dni = txtDni.getText().trim();
    String nombre = txtNombres.getText().trim();
    String telefono = txtTelefono.getText().trim();
    String email = txtEmail.getText().trim();
    String asunto = txtAsunto.getText().trim();
    String prioridad = (String) cmbPrioridad.getSelectedItem(); // o cmbPrioridad

    // Validaciones
    if (dni.length() != 8 || !dni.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "DNI inv√°lido (8 d√≠gitos num√©ricos)");
        return;
    }
    if (telefono.length() != 9 || !telefono.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Tel√©fono inv√°lido (9 d√≠gitos num√©ricos)");
        return;
    }
    if (!email.endsWith("mail.com")) {
        JOptionPane.showMessageDialog(this, "Email inv√°lido (debe terminar en mail.com)");
        return;
    }
    boolean esTrabajador = chkTrabajador.isSelected();

    Interesado interesado = new Interesado(dni, nombre, telefono, email, esTrabajador);
    Expediente exp = new Expediente(asunto, prioridad, interesado);
        // Guardar en cola seg√∫n prioridad
    switch (prioridad.toLowerCase()) {
        case "alta":
            GestorTramites.colaAlta.enqueue(exp);
            break;
        case "media":
            GestorTramites.colaMedia.enqueue(exp);
            break;
        case "baja":
            GestorTramites.colaBaja.enqueue(exp);
            break;
}

// Confirmaci√≥n
JOptionPane.showMessageDialog(this, "‚úÖ Expediente registrado correctamente.");
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnVerPendientesActionPerformedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerPendientesActionPerformedActionPerformed
    StringBuilder sb = new StringBuilder();

    sb.append("üö® Prioridad Alta:\n");
    mostrarCola(GestorTramites.colaAlta, sb);

    sb.append("\n‚ö†Ô∏è Prioridad Media:\n");
    mostrarCola(GestorTramites.colaMedia, sb);

    sb.append("\n‚úÖ Prioridad Baja:\n");
    mostrarCola(GestorTramites.colaBaja, sb);

    // Muestra la informaci√≥n en un JTextArea o en un pop-up
    JOptionPane.showMessageDialog(this, sb.toString());
}

// M√©todo para mostrar la cola
private void mostrarCola(Queue_LL<Expediente> cola, StringBuilder sb) {
    Queue_LL<Expediente>.Node actual = cola.getFront();
    if (actual == null) {
        sb.append(" (Sin expedientes en esta cola)\n");
        return;
    }
    while (actual != null) {
        sb.append(actual.data.toString()).append("\n--------------------------\n");
        actual = actual.next;
    }    // TODO add your handling code here:
    }//GEN-LAST:event_btnVerPendientesActionPerformedActionPerformed

